// Quotes
    // Single quotes: '
    // A typical string with single quotes
    'This is a String';

    // Double quotes: "
    // A typical string with double quotes
    "This is a String";

    // A single quote inside of a double quoted string
    "It's a String";

    // A single quote inside of a single quoted string
    'It\'s a String';

    // A double quote inside of a single quoted string
    'This is an "awesome" String';

    // A double quote inside of a double quoted string
    "This is an \"awesome\" String";

// Concatenation: 
    // Combining multiple strings to create a sentence
    'Hello' + ' ' + 'World';

    // Combining two string values by using concatenation
    'Hello '.concat('World');

    // Combining multiple strings and the concatenation method
    'Hello' + ' '.concat('World');

    // Using multiple stings inside of the concat function, to create a string
    'Hello'.concat(' ', 'World');

