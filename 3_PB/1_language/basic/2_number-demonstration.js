// Decimal Numbers
// https://developer.mozilla.org/en-US/docs/Learn/JavaScript/First_steps/Math

    // Integer (A whole number)
    100;
    5;
    7;
    31;

    // Float (A decimal number)
    8.5;
    7.6;
    1.2;
    3.5;

    // Negative Number
    - 4;
    - 7.3;

    // Plus: +

    3 + 5;
    3.5 + 8;

    // Minus: -
    8 - 4.2;
    6 - 2;

    // Multiplication: *
    3 * 7;
    8 * 4;
    2 * 3.5;
    1.2 * 3;

    // Division: /
    10 / 3;
    5 / 2;
    6 / 5;

    // Remainder (In other languages called Modulo)
    8 % 3;
    7 % 2;
    11 % 3;

    // Exponent (X multiplicated by itself Y times)
    5 ** 2;
    4 ** 3;

// Operator precedence
    // Browser reads: 1 + 2 + 5
    1 + 8 / 4 + 5;

    // Browser reads
    // 1 + 8 = 9
    // 4 + 5 = 9
    // 9 / 9
    (1 + 8) / (4 + 5);
