// var
    var testValue = 5;

    // a var can be redeclared
    var testValue = 10;

    // a var can be updated
    testValue = 20;

// let
    let testValue = 5;

    // a let can be updated
    testValue = 10;

    // a let can not be redeclared
    let testValue = 20;

// const
    const testValue = 5;

    // a const can not be updated
    testValue = 10;

    // a const can not be redeclared
    const testValue = 20;
